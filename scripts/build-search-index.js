#!/usr/bin/env node
/*
 * Builds js/search-index.js — a static, embeddable search index so the
 * site search works everywhere, including local file:// previews where
 * fetch() is blocked. Re-run this whenever page content changes:
 *
 *     node scripts/build-search-index.js
 *
 * It mirrors the in-browser indexing logic: index every element that has
 * an id and contains a heading, but only "leaf" blocks (skip wrappers that
 * contain a deeper id'd section with its own heading) to avoid duplicates.
 */
const fs = require('fs');
const path = require('path');

const ROOT = path.resolve(__dirname, '..');
const PAGES = ['index.html', 'experience.html', 'research.html', 'awards.html', 'interests.html'];

const VOID = new Set(['img', 'input', 'br', 'hr', 'meta', 'link', 'source', 'area', 'base', 'col', 'embed', 'param', 'track', 'wbr']);

function decode(s) {
  return s
    .replace(/&nbsp;/gi, ' ')
    .replace(/&amp;/gi, '&').replace(/&lt;/gi, '<').replace(/&gt;/gi, '>')
    .replace(/&quot;/gi, '"').replace(/&#0*39;/gi, "'").replace(/&apos;/gi, "'")
    .replace(/&#(\d+);/g, (m, n) => { try { return String.fromCodePoint(+n); } catch (e) { return ' '; } });
}

function stripText(html) {
  return decode(
    html.replace(/<(script|style)[\s\S]*?<\/\1>/gi, ' ').replace(/<[^>]+>/g, ' ')
  ).replace(/\s+/g, ' ').trim();
}

// Find the [start,end) char range of the element whose opening tag starts at `openStart`.
function rangeOf(html, openStart, tag) {
  const afterOpen = html.indexOf('>', openStart) + 1;
  if (VOID.has(tag)) return [openStart, afterOpen];
  const re = new RegExp('<' + tag + '(?=[\\s/>])|</' + tag + '\\s*>', 'gi');
  re.lastIndex = afterOpen;
  let depth = 1, m;
  while ((m = re.exec(html))) {
    if (m[0][1] === '/') { depth--; if (depth === 0) return [openStart, re.lastIndex]; }
    else depth++;
  }
  return [openStart, html.length];
}

function firstHeading(html) {
  const m = html.match(/<h[1-3][^>]*>([\s\S]*?)<\/h[1-3]>/i);
  return m ? stripText(m[1]) : '';
}

const entries = [];

for (const page of PAGES) {
  const html = fs.readFileSync(path.join(ROOT, page), 'utf8');
  const idRe = /<([a-zA-Z][\w-]*)\b[^>]*?\sid="([^"]+)"[^>]*>/g;
  const blocks = [];
  let m;
  while ((m = idRe.exec(html))) {
    const tag = m[1].toLowerCase();
    const id = m[2];
    const [start, end] = rangeOf(html, m.index, tag);
    const full = html.slice(start, end);
    blocks.push({ id, start, end, full, hasHeading: /<h[1-3][\s>]/i.test(full) });
  }
  for (const b of blocks) {
    if (!b.hasHeading) continue;
    // leaf check: skip if a deeper id'd block inside it also has a heading
    const hasInner = blocks.some(o => o !== b && o.start > b.start && o.end <= b.end && o.hasHeading);
    if (hasInner) continue;
    const title = firstHeading(b.full);
    const text = stripText(b.full);
    if (!text) continue;
    entries.push({ page, hash: b.id, title, text });
  }
}

const out =
  '/* AUTO-GENERATED by scripts/build-search-index.js — do not edit by hand.\n' +
  '   Regenerate after content changes:  node scripts/build-search-index.js */\n' +
  'window.SEARCH_INDEX = ' + JSON.stringify(entries, null, 0) + ';\n';

fs.writeFileSync(path.join(ROOT, 'js', 'search-index.js'), out);
console.log('Wrote js/search-index.js with ' + entries.length + ' entries:');
entries.forEach(e => console.log('  ' + e.page + '#' + e.hash + '  «' + e.title + '»  (' + e.text.length + ' chars)'));
